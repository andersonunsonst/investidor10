    public function index()
    {
        $search = request('search');
        if($search){
            $noticias = Noticia::query()
            ->where('titulo', 'LIKE', "%{$search}%")
            ->orWhere('descricao', 'LIKE', "%{$search}%")
            ->get();
        }else{
            $noticias = Noticia::all();
        }

        return view('noticias.index', [
            'noticias'=> $noticias
        ]);
     
    }


